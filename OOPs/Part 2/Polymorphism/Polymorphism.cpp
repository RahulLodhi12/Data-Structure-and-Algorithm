/*
Polymorphism :-
"Polymorphism" is the combination of "poly" + "morphs" which means many forms.
When a single thing is existing in multiple forms, then we call it Polymorphism.

There are two types of polymorphism in C++:
1. Compile time polymorphism
2. Run time polymorphism

NOTE: Compile-time is the time at which the source code(c++, java, python, etc) is converted into an executable code 
while the run time is the time at which the executable code is started running.

*/